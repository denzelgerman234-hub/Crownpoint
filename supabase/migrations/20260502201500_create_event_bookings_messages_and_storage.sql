create table if not exists public.event_booking_requests (
  id bigint generated by default as identity primary key,
  auth_user_id uuid references auth.users(id) on delete set null,
  user_public_id bigint references public.user_profiles(public_id) on delete set null,
  talent_id bigint not null references public.talents(id) on delete cascade,
  talent_name text not null default '',
  celebrity_name text not null default '',
  event_date date,
  approximate_budget text not null default '',
  event_type text not null default '',
  event_location text not null default '',
  additional_info text not null default '',
  full_name text not null default '',
  organization_name text not null default '',
  job_title text not null default '',
  phone_number text not null default '',
  email_address text not null default '',
  full_address text not null default '',
  nearest_airport text not null default '',
  status text not null default 'NEW',
  reviewed_at timestamptz,
  submitted_at timestamptz not null default timezone('utc', now()),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint event_booking_requests_status_check
    check (status in ('NEW', 'IN_REVIEW', 'IN_TOUCH', 'CLOSED'))
);

create index if not exists event_booking_requests_auth_user_id_idx
  on public.event_booking_requests (auth_user_id);
create index if not exists event_booking_requests_user_public_id_idx
  on public.event_booking_requests (user_public_id);
create index if not exists event_booking_requests_talent_id_idx
  on public.event_booking_requests (talent_id);
create index if not exists event_booking_requests_status_idx
  on public.event_booking_requests (status);
create index if not exists event_booking_requests_submitted_at_idx
  on public.event_booking_requests (submitted_at desc);

drop trigger if exists set_event_booking_requests_updated_at on public.event_booking_requests;

create trigger set_event_booking_requests_updated_at
before update on public.event_booking_requests
for each row
execute function public.set_updated_at();

alter table public.event_booking_requests enable row level security;

drop policy if exists "Users can read own event booking requests" on public.event_booking_requests;
create policy "Users can read own event booking requests"
on public.event_booking_requests
for select
using (auth.uid() = auth_user_id or public.is_admin());

drop policy if exists "Users can insert own event booking requests" on public.event_booking_requests;
create policy "Users can insert own event booking requests"
on public.event_booking_requests
for insert
with check (
  public.is_admin()
  or auth.uid() = auth_user_id
  or auth_user_id is null
);

drop policy if exists "Admins can update event booking requests" on public.event_booking_requests;
create policy "Admins can update event booking requests"
on public.event_booking_requests
for update
using (public.is_admin())
with check (public.is_admin());

drop policy if exists "Admins can delete event booking requests" on public.event_booking_requests;
create policy "Admins can delete event booking requests"
on public.event_booking_requests
for delete
using (public.is_admin());

create table if not exists public.message_threads (
  id bigint generated by default as identity primary key,
  fan_auth_user_id uuid not null references auth.users(id) on delete cascade,
  fan_user_public_id bigint references public.user_profiles(public_id) on delete cascade,
  fan_name text not null default '',
  fan_email text not null default '',
  talent_id bigint not null references public.talents(id) on delete cascade,
  talent_name text not null default '',
  topic text not null default 'Private inbox',
  preview text not null default '',
  created_at timestamptz not null default timezone('utc', now()),
  last_active_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint message_threads_fan_talent_unique unique (fan_auth_user_id, talent_id)
);

