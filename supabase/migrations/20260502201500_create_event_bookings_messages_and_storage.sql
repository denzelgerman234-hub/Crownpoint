create policy "Authenticated and guest uploads can write payment proofs"
on storage.objects
for insert
with check (
  bucket_id = 'payment-proofs'
  and (
    public.is_admin()
    or auth.uid()::text = (storage.foldername(name))[1]
    or (
      auth.role() = 'anon'
      and (storage.foldername(name))[1] = 'guest'
    )
  )
);

