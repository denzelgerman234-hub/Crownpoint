create table if not exists public.thread_messages (
  id bigint generated by default as identity primary key,
  thread_id bigint not null references public.message_threads(id) on delete cascade,
  sender_role text not null default 'system',
  sender_label text not null default 'System',
  message_text text not null default '',
  attachments jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint thread_messages_sender_role_check
    check (sender_role in ('fan', 'talent', 'system'))
);

